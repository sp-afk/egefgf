#include "ft_printf.h"

int	put_nbr(int n)
{
	int	c;

	c = 0;
	if (n == -2147483648)
	{
		c += put_char('-');
		c += put_char('2');
		n = 147483648;
	}
	if (n < 0)
	{
		c += put_char('-');
		n = -n;
	}
	if (n >= 10)
	{
		c += put_nbr((n / 10));
		c += put_nbr((n % 10));
	}
	else
		c += put_char(n + 48);
	return (c);
}
